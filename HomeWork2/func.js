// Функция которая принимает 2 числа и возвращает их сумму
function getSum(num1, num2) {
  return num1 + num2;
}

// Функция которая принимает 2 числа и возвращает их разность
function getSub(num1, num2) {
  return num1 - num2;
}

// Функция которая принимает 2 числа и возвращает их произведение
function getMil(num1, num2) {
  return num1 * num2;
}

// Функция которая принимает 2 числа и возвращает их сумму частное
function getDiv(num1, num2) {
  return num1 / num2;
}
